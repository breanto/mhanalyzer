public class DamageData {

    int cut;

    int impact;

    int bullet;

    int fire;

    int water;

    int ice;

    int thunder;

    int dragon;

    int dizzy;

    /**
     * Get Dizzy damage value
     * @return dizzy
     */
    public int getDizzy() {
        return dizzy;
    }

    /**
     * Set dizzy damage value
     * @param dizzy
     */
    public void setDizzy(int dizzy) {
        this.dizzy = dizzy;
    }

    /**
     * get cut damage value
     * @return cut
     */
    public int getCut() {
        return cut;
    }

    /**
     * Set cut damage value
     * @param cut
     */
    public void setCut(int cut) {
        this.cut = cut;
    }

    /**
     * get impact damage value
     * @return
     */
    public int getImpact() {
        return impact;
    }

    /**
     * Set impact damage value
     * @param impact
     */
    public void setImpact(int impact) {
        this.impact = impact;
    }

    /**
     * get bullet damage value
     * @return
     */
    public int getBullet() {
        return bullet;
    }

    /**
     * set bullet damage value
     * @param bullet
     */
    public void setBullet(int bullet) {
        this.bullet = bullet;
    }

    /**
     * get fire damage value
     * @return
     */
    public int getFire() {
        return fire;
    }

    /**
     * set fire damage value
     * @param fire
     */
    public void setFire(int fire) {
        this.fire = fire;
    }

    /**
     * get water damage value
     * @return
     */
    public int getWater() {
        return water;
    }

    /**
     * set water damage value
     * @param water
     */
    public void setWater(int water) {
        this.water = water;
    }

    /**
     * get ice damage value
     * @return
     */
    public int getIce() {
        return ice;
    }

    /**
     * set ice damage value
     * @param ice
     */
    public void setIce(int ice) {
        this.ice = ice;
    }

    /**
     * get thunder damage value
     * @return
     */
    public int getThunder() {
        return thunder;
    }

    /**
     * set thunder damage value
     * @param thunder
     */
    public void setThunder(int thunder) {
        this.thunder = thunder;
    }

    /**
     * get dragon damage value
     * @return
     */
    public int getDragon() {
        return dragon;
    }

    /**
     * set dragon damage value
     * @param dragon
     */
    public void setDragon(int dragon) {
        this.dragon = dragon;
    }
}
